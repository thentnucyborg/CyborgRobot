#include "Aria.h"

int main(int argc, char **argv)
{
  Aria::init();
  ArArgumentParser parser(&argc, argv);
  parser.loadDefaultArguments();
  ArRobot robot;

  robot.setLogSIPContents(true);

  // Connect to the robot, get some initial data from it such as type and name,
  // and then load parameter files for this robot.
  ArRobotConnector robotConnector(&parser, &robot);
  if(!robotConnector.connectRobot())
  {
    ArLog::log(ArLog::Terse, "Could not connect to the robot.");
    if(parser.checkHelpAndWarnUnparsed())
    {
        // -help not given
        Aria::logOptions();
        Aria::exit(1);
    }
  }

  if (!Aria::parseArgs() || !parser.checkHelpAndWarnUnparsed())
  {
    Aria::logOptions();
    Aria::exit(1);
  }

  ArActionGroupRatioDrive driveAct(&robot);
  driveAct.activate();
   
  robot.enableMotors();

  robot.runAsync(true);

  while(true)
  {
	  ArUtil::sleep(100);
	  robot.lock();
	  int f = robot.getFlags();
	  printf("Charging power good: %d, E-Stall Engaged: %d, Motors Enabled: %d, Stop Button Pressed: %d, Pioner Sonar Array 1: %d\n",  f&ArUtil::BIT10, f&ArUtil::BIT6, f&ArUtil::BIT0, f&ArUtil::BIT5, f&ArUtil::BIT1);
	  robot.unlock();
  }
  robot.waitForRunExit();

  Aria::exit(0);
}
